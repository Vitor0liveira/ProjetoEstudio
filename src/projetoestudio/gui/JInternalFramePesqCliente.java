    private void resetPanelComponents(JPanel panel) {
        disablePanelComponents(panel, false);
    }

    private void disablePanelComponents(JPanel panel, boolean value) {
        disablePanelComponents(panel, value, new Component[0]);
    }

    private void disablePanelComponents(JPanel panel, boolean value, Component... editables) {
        resetPanelComponents(panel);
        List<Component> editablesList = Arrays.asList(editables);
        for (Component component : jPanel2.getComponents()) {
            if (editables == null || editablesList.contains(component)) {
                component.setEnabled(value);
            }
        }
    }

    public void setPosicao() {
        Dimension d = this.getDesktopPane().getSize();
        this.setLocation((d.width - this.getSize().width) / 2,
                (d.height - this.getSize().height) / 2);
    }
